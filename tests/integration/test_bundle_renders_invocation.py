"""End-to-end integration test for PR-35: bundle renders cli_invocation.

Proves the full chain:

    cmd_run (captures invocation) ->
    artifact saved to store (serialization preserves it) ->
    cmd_export --bundle (reads from store) ->
    bundle README contains "Generated by" section with the captured argv

This lights up the conditional render path PR-32 added speculatively in
``falsifyai/bundle/writer.py:_generate_readme``. Before PR-35 shipped the
``cli_invocation`` field, the conditional was always false; now it fires.

Also asserts the semantic-boundary disclaimer is present per the PR-35
capture contract: "what command produced the artifact, not a guarantee
that re-running will produce identical outputs."
"""

import argparse
import shlex
import zipfile
from pathlib import Path

import falsifyai.cli.export as cli_export
import falsifyai.cli.main as cli_main
import falsifyai.cli.run as cli_run_mod
from falsifyai.replay.sqlite_store import SQLiteStore
from tests.fixtures.mock_adapter import MockAdapter

_SMOKE_SPEC = Path(__file__).resolve().parents[1] / "fixtures" / "specs" / "run_smoke.yaml"


def _patch_adapter(monkeypatch) -> None:
    adapter = MockAdapter(default_response="Paris is the capital of France.")
    monkeypatch.setattr(cli_run_mod, "build_adapter", lambda _model: adapter)


def _do_run(monkeypatch, db_path: str, raw_argv: list[str]) -> str:
    """Run cmd_run via cli_main.main and return the new session_id."""
    _patch_adapter(monkeypatch)
    monkeypatch.setattr("sys.argv", raw_argv)
    rc = cli_main.main(raw_argv[1:])  # strip program name
    assert rc == 0
    with SQLiteStore(db_path) as store:
        return next(iter(store.query_sessions(limit=1))).session_id


def _export(session_id: str, db_path: str, bundle_path: Path) -> None:
    rc = cli_export.cmd_export(
        argparse.Namespace(
            session_id=session_id,
            bundle=str(bundle_path),
            store_path=db_path,
            spec_path=None,
            allow_corrupted=False,
            overwrite=False,
            exported_at="2026-05-24T12:00:00+00:00",
        )
    )
    assert rc == 0


def _read_readme(bundle_path: Path) -> str:
    with zipfile.ZipFile(bundle_path, "r") as zf:
        return zf.read("README.md").decode("utf-8")


# ---------------------------------------------------------------------------
# Full pipeline: run -> export -> bundle README contains Generated by
# ---------------------------------------------------------------------------


def test_bundle_readme_contains_generated_by_section(monkeypatch, tmp_path, capsys) -> None:
    db_path = str(tmp_path / "replays.db")
    raw_argv = [
        "/usr/local/bin/falsifyai",
        "run",
        str(_SMOKE_SPEC),
        "--store-path",
        db_path,
    ]
    session_id = _do_run(monkeypatch, db_path, raw_argv)
    capsys.readouterr()

    bundle_path = tmp_path / "out.fai.zip"
    _export(session_id, db_path, bundle_path)
    readme = _read_readme(bundle_path)

    assert "## Generated by" in readme


def test_bundle_readme_renders_shell_quoted_command(monkeypatch, tmp_path, capsys) -> None:
    """The README must render the captured argv as a shell-friendly command line."""
    db_path = str(tmp_path / "replays.db")
    raw_argv = [
        "/usr/local/bin/falsifyai",
        "run",
        str(_SMOKE_SPEC),
        "--store-path",
        db_path,
    ]
    session_id = _do_run(monkeypatch, db_path, raw_argv)
    capsys.readouterr()

    bundle_path = tmp_path / "out.fai.zip"
    _export(session_id, db_path, bundle_path)
    readme = _read_readme(bundle_path)

    expected_command = shlex.join(["falsifyai", "run", str(_SMOKE_SPEC), "--store-path", db_path])
    assert expected_command in readme


def test_bundle_readme_includes_falsifyai_version(monkeypatch, tmp_path, capsys) -> None:
    """The README must surface the falsifyai version pinned in the invocation."""
    from falsifyai import __version__ as runtime_version

    db_path = str(tmp_path / "replays.db")
    raw_argv = [
        "/usr/local/bin/falsifyai",
        "run",
        str(_SMOKE_SPEC),
        "--store-path",
        db_path,
    ]
    session_id = _do_run(monkeypatch, db_path, raw_argv)
    capsys.readouterr()

    bundle_path = tmp_path / "out.fai.zip"
    _export(session_id, db_path, bundle_path)
    readme = _read_readme(bundle_path)

    assert f"FalsifyAI version at capture: `{runtime_version}`" in readme


def test_bundle_readme_includes_determinism_disclaimer(monkeypatch, tmp_path, capsys) -> None:
    """Semantic boundary (PR-35 plan §1): 'what command produced the artifact,
    not a guarantee that re-running will produce identical outputs.'
    """
    db_path = str(tmp_path / "replays.db")
    raw_argv = [
        "/usr/local/bin/falsifyai",
        "run",
        str(_SMOKE_SPEC),
        "--store-path",
        db_path,
    ]
    session_id = _do_run(monkeypatch, db_path, raw_argv)
    capsys.readouterr()

    bundle_path = tmp_path / "out.fai.zip"
    _export(session_id, db_path, bundle_path)
    readme = _read_readme(bundle_path)

    # The disclaimer must call out that this is provenance, not replay determinism.
    assert "not a guarantee" in readme
    assert "re-running" in readme
    # And it must point at where replay determinism actually lives.
    assert "materialized_hash" in readme
    assert "bundle_id" in readme


# ---------------------------------------------------------------------------
# Negative: artifact without cli_invocation (pre-PR-35) → no Generated by
# ---------------------------------------------------------------------------


def test_bundle_readme_omits_generated_by_for_pre_pr35_artifact(
    monkeypatch, tmp_path, capsys
) -> None:
    """Pre-PR-35 artifacts (cli_invocation=None) must NOT render Generated by.

    We synthesize a pre-PR-35 artifact by clearing the field on a freshly-built
    artifact before export (bypasses the SQLite round-trip by injecting via the
    in-memory store).
    """
    import dataclasses

    from falsifyai.replay.in_memory_store import InMemoryStore
    from falsifyai.spec.materializer import compute_materialized_hash
    from tests.fixtures.build_artifact import make_artifact

    # Build a clean artifact with NO cli_invocation (matches pre-PR-35 shape)
    artifact = make_artifact()
    correct_hash = compute_materialized_hash(artifact.materialized.cases)
    artifact = dataclasses.replace(
        artifact,
        materialized_hash=correct_hash,
        materialized=dataclasses.replace(artifact.materialized, materialized_hash=correct_hash),
        cli_invocation=None,  # explicit
    )

    store = InMemoryStore()
    store.save_session(artifact)

    monkeypatch.setattr(cli_export, "_build_store", lambda _p: store)

    bundle_path = tmp_path / "out.fai.zip"
    rc = cli_export.cmd_export(
        argparse.Namespace(
            session_id=artifact.session_id,
            bundle=str(bundle_path),
            store_path=":memory:",
            spec_path=None,
            allow_corrupted=False,
            overwrite=False,
            exported_at="2026-05-24T12:00:00+00:00",
        )
    )
    assert rc == 0

    readme = _read_readme(bundle_path)
    assert "## Generated by" not in readme
